public class EqualityRelationOperators {
    public static void main(String[] args) {
        // == =->operator de asignare
        int a = 5;
        int b = 7;
        System.out.println(a == b); //operator de egalitate

        System.out.println(a <= b); // mai mic sau egal
        System.out.println(a < b); //strict mai mic

        System.out.println(a >= b); //mai mare egal
        System.out.println(a > b); //strict mai mare

        System.out.println(a != b); // a diferit de b sau in eng NotEqual

    }
}
